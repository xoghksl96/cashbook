package vo;

public class Notice {
	private int noticeNo;
	private String noticeMemo;
	private String noticeupdatedate;
	private String noticecreatedate;
	
	
	public int getNoticeNo() {
		return noticeNo;
	}
	public void setNoticeNo(int noticeNo) {
		this.noticeNo = noticeNo;
	}
	public String getNoticeMemo() {
		return noticeMemo;
	}
	public void setNoticeMemo(String noticeMemo) {
		this.noticeMemo = noticeMemo;
	}
	public String getNoticeupdatedate() {
		return noticeupdatedate;
	}
	public void setNoticeupdatedate(String noticeupdatedate) {
		this.noticeupdatedate = noticeupdatedate;
	}
	public String getNoticecreatedate() {
		return noticecreatedate;
	}
	public void setNoticecreatedate(String noticecreatedate) {
		this.noticecreatedate = noticecreatedate;
	}	
}
